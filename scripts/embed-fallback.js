#!/usr/bin/env node
/**
 * embed-fallback.js — Generate fallback-data.js with embedded course text.
 *
 * Run this after updating course JSONs to refresh the embedded fallback data.
 * This keeps the plugin working in local dev mode without a remote URL.
 *
 * Usage:
 *   node scripts/embed-fallback.js
 *
 * Reads from: remote/courses/*.json
 * Writes to:  fallback-data.js (loaded by ui.html)
 */

const fs = require('fs');
const path = require('path');

const REMOTE_DIR = path.resolve(__dirname, '..', 'remote');
const INDEX_PATH = path.join(REMOTE_DIR, 'index.json');
const OUTPUT_PATH = path.resolve(__dirname, '..', 'fallback-data.js');

const index = JSON.parse(fs.readFileSync(INDEX_PATH, 'utf8'));

let output = '// Auto-generated by scripts/embed-fallback.js — do not edit manually\n';
output += 'var FALLBACK_COURSE_TEXT = {\n';

for (const course of index.courses) {
  const jsonPath = path.join(REMOTE_DIR, course.file);
  if (fs.existsSync(jsonPath)) {
    const text = fs.readFileSync(jsonPath, 'utf8');
    output += '  ' + JSON.stringify(course.id) + ': ' + text + ',\n';
  }
}

output += '};\n';

fs.writeFileSync(OUTPUT_PATH, output);
console.log('Generated fallback-data.js (' + Math.round(output.length / 1024) + ' KB)');
